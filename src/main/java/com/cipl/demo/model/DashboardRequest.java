package com.cipl.demo.model;
/**
 * @author Santosh Mahto
 *
 */
public class DashboardRequest {
	
	private String zoneCd;
	private String divisionCd;
	private String stationCode;
	private String trainTypeCode;
	private String tractionType;
	private String trainFrequencyCode;
	
	
	public String getZoneCd() {
		return zoneCd;
	}
	public void setZoneCd(String zoneCd) {
		this.zoneCd = zoneCd;
	}
	public String getDivisionCd() {
		return divisionCd;
	}
	public void setDivisionCd(String divisionCd) {
		this.divisionCd = divisionCd;
	}
	public String getStationCode() {
		return stationCode;
	}
	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}
	public String getTrainTypeCode() {
		return trainTypeCode;
	}
	public void setTrainTypeCode(String trainTypeCode) {
		this.trainTypeCode = trainTypeCode;
	}
	public String getTractionType() {
		return tractionType;
	}
	public void setTractionType(String tractionType) {
		this.tractionType = tractionType;
	}
	public String getTrainFrequencyCode() {
		return trainFrequencyCode;
	}
	public void setTrainFrequencyCode(String trainFrequencyCode) {
		this.trainFrequencyCode = trainFrequencyCode;
	}
	
	
	

}
